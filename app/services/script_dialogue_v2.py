def _extract_responsible_name(user_text: str) -> str:
    """Достаёт имя из реплики вроде «Я отвечаю, Иван Иванович».

    Возвращает имя с заглавных букв или пустую строку, если имени нет.
    """
    words = re.findall(r"[а-яёa-z]+", user_text.lower())
    name_parts = [
        w for w in words
        if w not in _NAME_STOPWORDS and w not in _NOT_A_NAME and len(w) >= 3
    ]
    if not name_parts:
        return ""
    # Берём не более двух слов (имя + отчество)
    return " ".join(p.capitalize() for p in name_parts[:2])


def _looks_like_name(user_text: str) -> bool:
    """Похоже ли на ответ-имя на вопрос «как могу к вам обращаться?».

    Срабатывает для «Меня зовут …» или для одиночного слова-имени.
    """
    lower = user_text.lower()
    if "зовут" in lower or "величают" in lower:
        return True
    words = re.findall(r"[а-яёa-z]+", lower)
    if len(words) == 1 and len(words[0]) >= 3 and words[0] not in _NOT_A_NAME:
        return True
    return False


