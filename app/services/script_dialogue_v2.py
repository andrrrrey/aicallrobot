def _months_until_named(lower: str) -> int | None:
    """Сколько месяцев до названного месяца от текущего (0..11), либо None."""
    target = None
    for stem, num in _MONTH_TO_NUM:
        if stem in lower:
            target = num
            break
    if target is None:
        return None
    now = datetime.datetime.now().month
    return (target - now) % 12


