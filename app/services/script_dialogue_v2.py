    async def _classify_qualification(
        self, user_text: str, step: int, platform_pending: bool = False
    ) -> str:
        # Для под-вопроса шага 3 (площадка → можно ли напрямую?) используем промпт 31
        if step == 3 and platform_pending:
            template = _QUAL_PROMPTS.get(31)
            valid = QUAL_CONTRACT_PLATFORM_CODES
        else:
            template = _QUAL_PROMPTS.get(step)
            valid = {
                0: QUAL_STEP0_CODES,
                1: QUAL_STEP1_CODES,
                2: QUAL_CONTRACTOR_CODES,
                3: QUAL_CONTRACT_CODES,
                5: QUAL_STEP5_CODES,
            }.get(step, ("unknown",))
        if not template:
            return "unknown"
        prompt = template.format(user_text=user_text)
        return await self._classify(prompt, valid)

    async def _classify(self, prompt: str, valid_codes: tuple | set) -> str:
        try:
            messages = [{"role": "user", "text": prompt}]
            result = await self.gpt.complete(messages, temperature=0.05, max_tokens=12)
            code = result.strip().lower().rstrip(".,!?").strip()
            if code in valid_codes:
                return code
            # Попытка найти код как подстроку
            for c in valid_codes:
                if c in code:
                    return c
            return "unknown"
        except Exception as e:
            logger.error(f"[v2] classify error: {e}")
            return "unknown"

